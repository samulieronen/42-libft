#include "libft.h"

void	ft_putnbr(int n)
{
	long i;

	i = n;
	if (i < 0)
	{
		write(1, "-", 1);
		i = i * -1;
	}
	if (i >= 0 && i < 10)
	{
		i = i + 48;
		ft_putchar(i);
	}
	else if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
