int		ft_number_len(int n)
{
	int		len;
	long	nb;

	nb = n;
	len = 1;
	if (nb < 0)
	{
		len++;
		nb = nb * -1;
	}
	while (nb > 9)
	{
		len++;
		nb = nb / 10;
	}
	return (len);
}
